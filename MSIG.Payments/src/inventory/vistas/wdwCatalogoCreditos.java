    private void cbxSuscripcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxSuscripcionActionPerformed
        ArrayList<ObjetosSuscripcion> lista = new ArrayList();
        lista = acceso_suscripcion.mostrarSuscripcionesPorCliente(cbxSuscripcion.getSelectedItem().toString());
        txtCantidadSuscripciones.setText(String.valueOf(lista.size()));
        cbxIdSuscripcion.removeAllItems();
        for (ObjetosSuscripcion x : lista) {
            cbxIdSuscripcion.addItem(x.getId_suscripcion());  
        }
    }//GEN-LAST:event_cbxSuscripcionActionPerformed

    private void NoCuotasLostFocus(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_NoCuotasLostFocus
        Double resultado = Double.parseDouble(txtMonto.getText()) / Double.parseDouble(txtNoCuotas.getText());
        txtMontoCuota.setText(String.valueOf(matematicas.redondear(resultado, 2)));
        txtSaldo.setText(txtMonto.getText());
    }//GEN-LAST:event_NoCuotasLostFocus

    private void cbxIdSuscripcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxIdSuscripcionActionPerformed
        //buscar la direccion de las suscripcion
        ArrayList<ObjetosSuscripcion> lista = new ArrayList<>();
        if(cbxIdSuscripcion.getItemCount() > 0) {
            lista = acceso_suscripcion.buscarInformacionSuscripcion(cbxIdSuscripcion.getSelectedItem().toString());
            for(ObjetosSuscripcion x : lista) {
                txtDireccion.setText(x.getDireccion_suscripcion());
            }
        }
    }//GEN-LAST:event_cbxIdSuscripcionActionPerformed

