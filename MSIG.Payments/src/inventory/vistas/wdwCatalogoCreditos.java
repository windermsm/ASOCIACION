    private void buscarRegistros() {

        DefaultTableModel tabla = new DefaultTableModel();
        ArrayList<ObjetosCredito> lista_creditos = new ArrayList();

        lista_creditos = acceso_credito.listarCredito(txtBuscar.getText());

        tabla.addColumn("ID Credito");
        tabla.addColumn("ID Suscripcion");
        tabla.addColumn("Cliente");
        tabla.addColumn("Direccion");
        tabla.addColumn("Descripcion");
        tabla.addColumn("Monto");
        tabla.addColumn("No. Cuotas");
        tabla.addColumn("Monto de Cuotas");
        tabla.addColumn("Saldo");
        tabla.addColumn("Abonado");
        tabla.addColumn("Estado");

        tabla.setRowCount(lista_creditos.size());
        int fila = 0;

        for (ObjetosCredito x : lista_creditos) {
            tabla.setValueAt(x.getId_credito(), fila, 0);
            tabla.setValueAt(x.getId_suscripcion(), fila, 1);
            tabla.setValueAt(x.getNombre_cliente(), fila, 2);
            tabla.setValueAt(x.getDireccion_suscripcion(), fila, 3);
            tabla.setValueAt(x.getDescripcion_credito(), fila, 4);
            tabla.setValueAt(x.getMonto_credito(), fila, 5);
            tabla.setValueAt(x.getCantidad_cuotas_credito(), fila, 6);
            tabla.setValueAt(x.getMonto_cuota_credito(), fila, 7);
            tabla.setValueAt(x.getSaldo_credito(), fila, 8);
            tabla.setValueAt(x.getAbonado_credito(), fila, 9);
            tabla.setValueAt(x.getEstado_credito(), fila, 10);
            fila++;
        }

        tblCreditos.setModel(tabla);
    }

    private void guardarRegistros() {
        credito.setId_credito(0);
        credito.setId_suscripcion(Integer.parseInt(cbxIdSuscripcion.getSelectedItem().toString()));
        credito.setNombre_cliente(cbxSuscripcion.getSelectedItem().toString());
        credito.setDireccion_suscripcion(txtDireccion.getText());
        credito.setDescripcion_credito(txtDescripcion.getText());
        credito.setMonto_credito(Double.parseDouble(txtMonto.getText()));
        credito.setCantidad_cuotas_credito(Integer.parseInt(txtNoCuotas.getText()));
        credito.setMonto_cuota_credito(Double.parseDouble(txtMontoCuota.getText()));
        credito.setSaldo_credito(Double.parseDouble(txtSaldo.getText()));
        credito.setAbonado_credito(Double.parseDouble(txtAbonado.getText()));
        credito.setEstado_credito(cbxEstado.getSelectedItem().toString());
        acceso_credito.insertarCredito(credito, Inventory.txtUsuario.getText());
    }


