    private void comprobarUsuario() {

        ArrayList<ObjetosUsuario> lista = new ArrayList<>();
        AccesoUsuario acceso = new AccesoUsuario();
        lista = acceso.ingresoUsuario(txtUsuario.getText(), txtContrasenia.getText());

        if (lista.size() == 0) {
            mensaje.manipulacionExcepciones("critico", "El usuario y la contraseÃ±a son incorrectos");
        } else {
            if (lista.size() > 1) {
                mensaje.manipulacionExcepciones("critico", "Exixten mas de un usuario con estas credenciales, verifique");
            } else {

                Inventory.txtUsuario.setText(txtUsuario.getText());

                for (ObjetosUsuario x : lista) {
                    if (x.getTipo_usuario().equals("Administrador")) {
                        Inventory.menu.setVisible(true);
                        Inventory.menuMovimiento.setVisible(true);
                        Inventory.menu.setVisible(true);
                        Inventory.menuReportes.setVisible(true);
                        this.dispose();
                    }

                    if (x.getTipo_usuario().equals("Jefe Sucursal")) {
                        Inventory.menu.setVisible(true);
                        Inventory.menuMovimiento.setVisible(true);
                        Inventory.menuCatalogo.setVisible(true);
                        Inventory.menuReportes.setVisible(true);
                        this.dispose();
                    }

                    if (x.getTipo_usuario().equals("Secretaria")) {
                        Inventory.menu.setVisible(true);
                        Inventory.menuMovimiento.setVisible(true);
                        Inventory.menuCatalogo.setVisible(true);
                        Inventory.menuReportes.setVisible(false);
                        this.dispose();
                    }

                    if (x.getTipo_usuario().equals("Cajero")) {
                        Inventory.menu.setVisible(true);
                        Inventory.menuMovimiento.setVisible(true);
                        Inventory.menuCatalogo.setVisible(false);
                        Inventory.menuReportes.setVisible(false);
                        this.dispose();
                    }
                }
            }
        }

        txtContrasenia.setText("");
        txtUsuario.setText("");
        txtUsuario.requestFocus();
    }

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (txtUsuario.getText().equals("admin") && txtContrasenia.getText().equals("Derwin2012mg")) {
            Inventory.menu.setVisible(true);
            Inventory.txtUsuario.setText("admin");
            this.dispose();
        } else {
            comprobarUsuario();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void txtContraseniaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraseniaKeyPressed
        if (evt.getKeyCode() == 10) {
            comprobarUsuario();
        }
    }//GEN-LAST:event_txtContraseniaKeyPressed
