    private void btnDescargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescargaActionPerformed

        //variables para el servicio web
        String anio = txtAnioLectura.getText();
        String mes = txtMesLectura.getText();
        String ser = txtIdServicio.getText();
        String empresa = "Oportunos";

        try {
            java.net.URL url = new java.net.URL("http://www.oportunosgt.com/payments/services/ejecutar.php?P_FUNCTION=fn_prueba_descarga&P_PARAMETERS='" + empresa + "'," + ser + "," + anio + "," + mes);
            System.out.println("URL: " + "http://www.oportunosgt.com/payments/services/ejecutar.php?P_FUNCTION=fn_prueba_descarga&P_PARAMETERS='" + empresa + "'," + ser + "," + anio + "," + mes);
            java.net.URLConnection con = url.openConnection();
            InputStream s = con.getInputStream();
            BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(s));
            System.out.println("Contenido Pagina Web:");
            txtJSON.setText("Datos Obtenidos");
            LeerJSON leer = new LeerJSON();
            listado = leer.obtenerJSON(bufferedReader.readLine());
        } catch (java.net.MalformedURLException e) {
            mensaje.manipulacionExcepciones("critico", e.getMessage());
        } catch (IOException e) {
            mensaje.manipulacionExcepciones("critico", e.getMessage());
        }
        
        //mostrar el listado descargado en pantalla
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("ID Lectura");
        tabla.addColumn("ID Suscripcion");
        tabla.addColumn("AÃ±o");
        tabla.addColumn("Mes");
        tabla.addColumn("Lectura Anterior");
        tabla.addColumn("Lectura Nueva");
        tabla.addColumn("Consumo Total");
        tabla.setRowCount(listado.size());

        System.out.println("Objetos Descargados: " + listado.size());

        int fila = 0;

        for (ObjetosLectura x : listado) {
            try {
                tabla.setValueAt(fila + 1, fila, 0);
                tabla.setValueAt(x.getId_suscripcion(), fila, 1);
                tabla.setValueAt(x.getAnio_lectura(), fila, 2);
                tabla.setValueAt(x.getMes_lectura(), fila, 3);
                tabla.setValueAt(x.getAnterior_lectura(), fila, 4);
                tabla.setValueAt(x.getNueva_lectura(), fila, 5);
                tabla.setValueAt(x.getNueva_lectura() - x.getAnterior_lectura(), fila, 6);
                fila++;
            } catch (Exception e) {
                System.out.println("Error al mostrar valor de la suscripcion " + x.getId_suscripcion() + ": " + e.getMessage());
            }
        }

        tblLecturas.setModel(tabla);
    }//GEN-LAST:event_btnDescargaActionPerformed
