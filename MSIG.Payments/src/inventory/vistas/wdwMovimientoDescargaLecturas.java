    private void guardarRegistros() {
        //variable que almacena los registros almacenados
        int registros_guardados = 0;
        //recorrer la tabla para guardar todos los registros
        for (int x = 0; x < listado.size(); x++) {
            try {
                //crear objeto con la informacion segun la descarga
                lectura.setId_suscripcion(Integer.parseInt(tblLecturas.getValueAt(x, 1).toString()));
                lectura.setAnio_lectura(Integer.parseInt(tblLecturas.getValueAt(x, 2).toString()));
                lectura.setMes_lectura(Integer.parseInt(tblLecturas.getValueAt(x, 3).toString()));
                lectura.setAnterior_lectura(Integer.parseInt(tblLecturas.getValueAt(x, 4).toString()));
                lectura.setNueva_lectura(Integer.parseInt(tblLecturas.getValueAt(x, 5).toString()));
                lectura.setEstado_lectura("A");
                acceso_lectura.insertarLectura(lectura, "webservice");
                System.out.println("Guardada: " + tblLecturas.getValueAt(x, 0) + " Suscripcion: " + tblLecturas.getValueAt(x, 1));
                registros_guardados++;
            } catch (Exception e) {
                System.out.println("Error al guardar: " + tblLecturas.getValueAt(x, 0) + " Suscripcion: " + tblLecturas.getValueAt(x, 1));
            }
        }
        mensaje.manipulacionExcepciones("informacion", "Registros guardados " + registros_guardados);
    }

